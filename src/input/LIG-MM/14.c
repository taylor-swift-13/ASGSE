#include <stdlib.h>
struct sys_slist_t {
    struct sys_slist_t *next;
};


struct sys_slist_t *main14(struct sys_slist_t *p)
{
    struct sys_slist_t *w, *t, *v;
    w = 0;
    v = p;
    while (v) {
        t = v->next;
        v->next = w;
        w = v;
        v = t;
    }
    /*@ assert p == \at(p,Pre); */
    return w;
}